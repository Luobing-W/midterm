# ============================================
# 5. Server Logic
# ============================================
server <- function(input, output, session) {
  
  # ===== Event: Submit Form → Write to Database =====
  observeEvent(input$submit_btn, {
    # Validate Input
    if (input$location_detail == "") {
      showNotification("⚠️ Please fill in the specific location", type = "warning")
      return()
    }
    
    # Prepare New Record
    new_record <- data.frame(
      report_date = as.Date(input$report_date),
      park_name = input$park_name,
      location_detail = input$location_detail,
      injury_type = input$injury_type,
      severity_level = as.integer(input$severity_level),
      description = input$description,
      stringsAsFactors = FALSE
    )
    
    # Write to Database (Using dbAppendTable to append data)
    con <- NULL
    tryCatch({
      con <- get_con()
      dbAppendTable(con, "park_injuries", new_record)
      output$submit_status <- renderText({"✅ Submission Successful!"})
      showNotification("Report submitted successfully!", type = "message")
      
      # Clear partial form
      updateTextInput(session, "location_detail", value = "")
      updateTextInput(session, "description", value = "")
      updateNumericInput(session, "severity_level", value = 2)
      
    }, error = function(e) {
      output$submit_status <- renderText({paste("❌ Submission Failed:", e$message)})
      showNotification(paste("Submission Failed:", e$message), type = "error")
    }, finally = {
      if (!is.null(con)) dbDisconnect(con)
    })
  })
  
  # ===== Output: Total Count =====
  output$total_count <- renderText({
    con <- NULL
    tryCatch({
      con <- get_con()
      n <- dbGetQuery(con, "SELECT COUNT(*) as n FROM park_injuries")$n
      paste("📊 Total Reports:", n)
    }, finally = {
      if (!is.null(con)) dbDisconnect(con)
    })
  })
  
  # ===== Output: Average Severity =====
  output$avg_severity <- renderText({
    con <- NULL
    tryCatch({
      con <- get_con()
      avg <- dbGetQuery(con, "SELECT AVG(severity_level) as avg FROM park_injuries")$avg
      paste("⚠️ Average Severity:", round(avg, 2))
    }, finally = {
      if (!is.null(con)) dbDisconnect(con)
    })
  })
  
  # ===== Output: Recent Records Table =====
  output$recent_records <- renderTable({
    con <- NULL
    tryCatch({
      con <- get_con()
      data <- dbGetQuery(con, "
        SELECT report_date, park_name, location_detail, injury_type, severity_level, description 
        FROM park_injuries 
        ORDER BY report_date DESC 
        LIMIT 10
      ")
      
      # ✅ FIX DATE DISPLAY: Convert date to character format
      if(nrow(data) > 0) {
        data$report_date <- format(as.Date(data$report_date), "%Y-%m-%d")
      }
      
      return(data)
    }, finally = {
      if (!is.null(con)) dbDisconnect(con)
    })
  },
  na.strings = "",
  striped = TRUE,
  hover = TRUE,
  width = "100%",
  rownames = FALSE)
}

# ============================================
# 6. Run Application
# ============================================
shinyApp(ui = ui, server = server)
