library(shiny)
library(DBI)
library(RPostgres)
library(dplyr)

# ============================================
# 1. Load Environment Variables
# ============================================
readRenviron(".env")

# ============================================
# 2. Database Connection Function
# ============================================
get_con <- function() {
  dbConnect(
    RPostgres::Postgres(),
    dbname = Sys.getenv("SUPABASE_DB", "postgres"),
    host = Sys.getenv("SUPABASE_HOST"),
    port = as.integer(Sys.getenv("SUPABASE_PORT", "5432")),
    user = Sys.getenv("SUPABASE_USER", "postgres"),
    password = Sys.getenv("SUPABASE_PASSWORD")
  )
}

# ============================================
# 3. Initialize Database (Create Table + Seed Data)
# ============================================
init_database <- function() {
  con <- get_con()
  
  # Check if table exists
  tables <- dbListTables(con)
  
  if (!"park_injuries" %in% tables) {
    cat("📋 Table 'park_injuries' does not exist. Creating...\n")
    
    # Create Table
    dbExecute(con, "
      CREATE TABLE park_injuries (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        report_date DATE NOT NULL,
        park_name TEXT NOT NULL,
        location_detail TEXT NOT NULL,
        injury_type TEXT NOT NULL,
        severity_level INTEGER CHECK (severity_level BETWEEN 1 AND 5),
        description TEXT,
        created_at TIMESTAMPTZ DEFAULT NOW()
      )
    ")
    
    # Insert Synthetic Data (Seed Data)
    synthetic_data <- data.frame(
      report_date = as.Date(c("2023-10-01", "2023-10-05", "2023-10-10")),
      park_name = c("Central Park", "Riverside Park", "Central Park"),
      location_detail = c("Playground", "Hiking Trail", "Basketball Court"),
      injury_type = c("Fall", "Sprain", "Collision"),
      severity_level = c(2, 3, 4),
      description = c("Child fell off swing", "Ankle twisted on rock", "Players collided"),
      stringsAsFactors = FALSE
    )
    
    dbAppendTable(con, "park_injuries", synthetic_data)
    cat("✅ Inserted", nrow(synthetic_data), "rows of synthetic data\n")
  } else {
    cat("✅ Table 'park_injuries' already exists\n")
  }
  
  # Verify and display record count
  count <- dbGetQuery(con, "SELECT COUNT(*) as n FROM park_injuries")$n
  cat("📊 Current total records:", count, "\n")
  
  dbDisconnect(con)
}

# 🚀 Initialize Database on Startup
init_database()

